<template lang="pug">
.flap-card-wrapper(v-show="flapCardVisible")
  .flap-card-bg(
    :class="{'animation': runFlapCardAnimation}"
    v-show="!runBookCardAnimation"
    )
    .flap-card(v-for="(item, index) in flapCardList"
    :key="index"
    :style="{ zIndex:item.zIndex }"
    )
      .flap-card-circle
        .flap-card-semi-circle.flap-card-semi-circle-left(
          :style="semiCircleStyle(item,'left')"
          ref="left"
        )
        .flap-card-semi-circle.flap-card-semi-circle-right(
          :style="semiCircleStyle(item,'right')"
          ref="right"
        )
    .point-wrapper
      .point(v-for="(item) in pointList"
       :key="item"
       :class="{'animation': runPointAnimation }")
  .book-card(
    :class="{'animation':runBookCardAnimation}"
    v-show="runBookCardAnimation"
    )
    .book-card-wrapper
      .img-wrapper
        img.img(:src="data.cover")
      .content-wrapper
        .content-title {{data ? data.title : ''}}
        .content-author.sub-title-medium {{ data ? data.author : ''}}
        .content-category {{ categoryText() }}
      .read-btn(@click.stop="showBookDetail(data)") {{$t('home.readNow')}}
  .close-btn-wrapper(@click="close")
    span.icon-close
</template>

<script>
import { storeHomeMixin } from '@/utils/mixin'
import { flapCardList, categoryText } from '@/utils/store'

export default {
  name: 'FlapCard',
  mixins: [storeHomeMixin],
  props: {
    data: {
      type: Object
    }
  },
  data() {
    return {
      flapCardList,
      front: 0,
      back: 1,
      intervalTime: 25,
      task: null,
      runFlapCardAnimation: false,
      runPointAnimation: false,
      pointList: null,
      runBookCardAnimation: false
    }
  },
  watch: {
    flapCardVisible(v) {
      if (v) {
        this.runAnimation()
      }
    }
  },
  methods: {
    /**
     * 关闭
     */
    close() {
      this.stopAnimation()
      this.setFlapCardVisible(false)
    },
    /**
     * 设置半圆的样式
     * @param {Object} item
     * @param {String} dir
     * @returns {{backgroundColor: string, backgroundImage: (*), backgroundSize: (string|string)}}
     */
    semiCircleStyle(item, dir) {
      return {
        backgroundColor: `rgb(${item.r}, ${item.g}, ${item.b})`,
        backgroundSize: item.backgroundSize,
        backgroundImage: dir === 'left' ? item.imgLeft : item.imgRight
      }
    },
    /**
     * 转动的核心方法
     * @param {Number} index 第几个圆
     * @param {String} type 是正面还是背面
     */
    rotate(index, type) {
      const item = this.flapCardList[index]
      let dom
      if (type === 'front') {
        dom = this.$refs.right[index]
      } else {
        dom = this.$refs.left[index]
      }
      dom.style.transform = `rotateY(${item.rotateDegree}deg)`
      dom.style.backgroundColor = `rgb(${item.r},${item._g},${item.b})`
    },
    /**
     * 转动卡片的核心方法
     */
    flapCardRotate() {
      const frontFlapCard = this.flapCardList[this.front]
      const backFlapCard = this.flapCardList[this.back]
      frontFlapCard.rotateDegree += 10
      frontFlapCard._g -= 5
      backFlapCard.rotateDegree -= 10
      // 当转动的角度小于90时,才开始变淡颜色
      if (backFlapCard.rotateDegree < 90) {
        backFlapCard._g += 5
      }
      // 第一个临界点: 两个半圆都为90deg
      if (frontFlapCard.rotateDegree === 90 && backFlapCard.rotateDegree === 90) {
        backFlapCard.zIndex += 2
      }
      this.rotate(this.front, 'front')
      this.rotate(this.back, 'back')
      // 第二个临界点: 上半圆转动180deg,下半圆转动为0时,调用下一次的卡片
      if (frontFlapCard.rotateDegree === 180 && backFlapCard.rotateDegree === 0) {
        this.next()
      }
    },
    /*
     *  索引加,并动态设置zindex 使之生效
     * */
    next() {
      const frontFlapCard = this.flapCardList[this.front]
      const backFlapCard = this.flapCardList[this.back]
      frontFlapCard._g = frontFlapCard.g
      frontFlapCard.rotateDegree = 0
      backFlapCard._g = backFlapCard.g
      backFlapCard.rotateDegree = 0
      this.rotate(this.front, 'front')
      this.rotate(this.back, 'back')
      this.front++
      this.back++
      const len = this.flapCardList.length
      if (this.front >= len) {
        this.front = 0
      }
      if (this.back >= len) {
        this.back = 0
      }
      // 动态设置z-index
      this.flapCardList.forEach((item, index) => {
        item.zIndex = 100 - ((index - this.front + len) % len)
      })
      this.prepare()
    },
    /*
     * 使下半圆与上半圆进行重合
     * */
    prepare() {
      const backFlapCard = this.flapCardList[this.back]
      backFlapCard.rotateDegree = 180
      backFlapCard._g = backFlapCard.g - 5 * 9
      this.rotate(this.back, 'back')
    },
    /*
     *  停止动画,以及重置样式
     * */
    stopAnimation() {
      if (this.task) {
        clearInterval(this.task)
      }
      if (this.timeout) {
        clearTimeout(this.timeout)
      }
      this.reset()
    },
    /*
     *  重置样式
     * */
    reset() {
      this.front = 0
      this.back = 1
      this.flapCardList.forEach((item, index) => {
        item.zIndex = 100 - index
        item._g = item.g
        item.rotateDegree = 0
        this.rotate(index, 'front')
        this.rotate(index, 'back')
      })
      this.runBookCardAnimation = false
      this.runFlapCardAnimation = false
      this.runPointAnimation = false
    },
    /*
     *  开始卡片翻转动画
     * */
    startFlapCardAnimation() {
      this.prepare()
      this.task = setInterval(() => {
        this.flapCardRotate()
      }, this.intervalTime)
    },
    runAnimation() {
      this.runFlapCardAnimation = true
      setTimeout(() => {
        this.startFlapCardAnimation()
        this.startPointAnimation()
      }, 300)

      //  模拟请求api
      this.timeout = setTimeout(() => {
        this.stopAnimation()
        this.runBookCardAnimation = true
      }, 2500)
    },
    startPointAnimation() {
      this.runPointAnimation = true
      setTimeout(() => {
        this.runPointAnimation = false
      }, 1500)
    },
    categoryText() {
      if (this.data) {
        return categoryText(this.data.category, this)
      } else {
        return ''
      }
    }
  },
  created() {
    this.pointList = []
    for (let i = 0; i < 18; i++) {
      this.pointList.push(`point${i}`)
    }
  }
}
</script>

