<template lang="pug">
  shelf-dialog(
    :title="title"
    ref="dialog"
  )
    .dialog-list-wrapper(v-if="!ifNewGroup")
      .dialog-list-item(
        :class="{ 'is-add': item.edit ? item.edit === 1 : false }"
        v-for="(item, index) in categoryList"
        :key="index"
        @click="onGroupClick(item)"
        v-if="(item.edit === 2 && isInGroup()) || item.edit !== 2 || !item.edit"
      )
        .dialog-list-item-text {{ item.title }}
        .dialog-list-icon-wrapper(v-if="shelfCategory.id === item.id && isInGroup()")
          span.icon-check
    .dialog-new-group-wrapper(v-else)
      .dialog-input-title-wrapper
        span.dialog-input-title {{ $t('shelf.groupName') }}
      .dialog-input-wrapper
        .dialog-input-inner-wrapper
          input.dialog-input(
            type="text"
            v-model="newGroupName"
            ref="dialogInput"
          )
          .dialog-input-clear-wrapper(
            @click="clear"
            v-show="newGroupName.length > 0"
          )
            span.icon-close-circle-fill
    template(#btn)
      .group-dialog-btn-wrapper
        .dialog-btn(@click="hide") {{ $t("shelf.cancel") }}
        .dialog-btn(
          @click="createNewGroup"
          :class="{ 'is-empty': newGroupName.length === 0 }"
          v-if="ifNewGroup"
        ) {{ $t("shelf.confirm") }}
</template>

<script>
import ShelfDialog from '@/components/common/Dialog'
import { storeShelfMixin } from '@/utils/mixin'
import { removeAddFromShelf, appendAddToShelf } from '@/utils/store'
import { saveBookShelf } from '@/utils/localStorage'

export default {
  name: 'ShelfGroupDialog',
  mixins: [storeShelfMixin],
  components: { ShelfDialog },
  computed: {
    defaultCategory() {
      return [
        {
          title: this.$t('shelf.newGroup'),
          edit: 1
        },
        {
          title: this.$t('shelf.groupOut'),
          edit: 2
        }
      ]
    },
    category() {
      return this.shelfList.filter(item => item.type === 2)
    },
    categoryList() {
      return [...this.defaultCategory, ...this.category]
    },
    title() {
      return !this.ifNewGroup ? this.$t('shelf.moveBook') : this.$t('shelf.newGroup')
    }
  },
  data() {
    return {
      ifNewGroup: false,
      newGroupName: ''
    }
  },
  methods: {
    isInGroup() {
      return this.currentType === 2
    },
    show() {
      this.$refs.dialog.show()
    },
    hide() {
      this.$refs.dialog.hide()
      this.ifNewGroup = false
    },

    onGroupClick(item) {
      if (item.edit && item.edit === 1) {
        this.ifNewGroup = true
      } else if (item.edit && item.edit === 2) {
        this.moveOutFromGroup(item)
      } else {
        this.moveToGroup(item)
      }
    },
    clear() {
      this.newGroupName = ''
    },
    moveToGroup(group) {
      this.setShelfList(
        this.shelfList.filter(book => {
          if (book.itemList) {
            book.itemList = book.itemList.filter(subBook => {
              return this.shelfSelected.indexOf(subBook) < 0
            })
          }
          return this.shelfSelected.indexOf(book) < 0
        })
      ).then(() => {
        if (group && group.itemList) {
          group.itemList = [...group.itemList, ...this.shelfSelected]
        }
        group.itemList.forEach((item, index) => {
          item.id = index + 1
        })
        this.simpleToast(this.$t('shelf.moveBookInSuccess').replace('$1', group.title))
        this.onComplete()
      })
    },
    moveOutFromGroup(item) {},
    createNewGroup() {
      if (!this.newGroupName && this.newGroupName.length === 0) {
        return
      }
      const group = {
        id: this.shelfList[this.shelfList.length - 2].id + 1,
        itemList: [],
        selected: false,
        title: this.newGroupName,
        type: 2
      }
      const list = removeAddFromShelf(this.shelfList)
      list.push(group)
      this.setShelfList(appendAddToShelf(list)).then(() => {
        this.onComplete()
      })
    },
    onComplete() {
      saveBookShelf(this.shelfList)
      this.hide()
      this.setIsEditMode(false)
    }
  }
}
</script>

